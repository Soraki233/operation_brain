class ExcelTableAnalyzePrompt:
    def get_prompt(self, table_title: str, table_data: str):
        return f"""
你是一名“docx表格语义化解析与知识库结构化抽取”专家。你的任务是把从 Word 文档中提取出的 XML 表格数组，解析为适合知识库入库、向量检索和后续问答使用的结构化 JSON 数据。

请注意：你不仅要生成语义化文本内容，还要同时抽取实体信息、字段信息和值信息。

## 输入背景
下面会提供一个表格标题，以及一个由 XML 提取得到的 table 数组。

其中：
- table 数组表示一个完整表格；
- 数组中的每个元素代表表格中的一行；
- 行中可能包含字段名、子字段名、参数值、备注、重复列内容，或因 Word/XML 解析产生的噪声；
- 某些值可能在多个列中重复出现，需要去重后再理解；
- 某些字段存在层级关系，需要识别并保留层级语义；
- 表格既可能是普通属性表，也可能是条件-结果映射表、分段表、横向矩阵表或复合参数表。

## 输出目标
请把表格中的每一条有效信息解析为一个 JSON 对象，并最终输出为 JSON 数组。

每个 JSON 对象必须包含以下字段：

{{
  "entity_type": "",
  "entity_name": "",
  "field_name": "",
  "field_value": "",
  "field_category": "",
  "content": ""
}}

## 核心抽取原则
1. 忠实原文，不得编造。
2. 允许轻量清洗格式噪声，但不得改变原始含义。
3. 优先保证结构化字段可用于程序处理，其次保证 content 可用于知识库检索。
4. 如果无法可靠判断某字段，宁可输出空字符串，也不要主观猜测。

## 字段含义与抽取规则

### 1. entity_type
表示当前数据所属的实体类别、设备类别或对象语义主体。
要求：
- 优先结合表格标题、表头、字段层级和行内容判断；
- 可以是通用设备类别，也可以是“上级对象 + 子部件”的组合实体；
- 当字段描述的是某设备的明确子部件、组成部分或局部参数时，应优先输出更完整的实体语义，而不是过于宽泛的上级对象；
- 例如：
  - 如果表格描述的是发电机中的定子电流关系，可输出“发电机定子”
  - 如果表格描述的是炉排片宽度，可输出“炉排片”
  - 如果表格描述的是液压油缸行程，可输出“液压油缸”
- 如果无法可靠判断，则输出空字符串。

### 2. entity_name
表示当前数据所属的具体对象名称、实例名称、设备编号或唯一主体标识。
要求：
- 只有当表格中明确出现了具体设备名称、编号、实例名、代号时，才填写 entity_name；
- 如果表格只描述通用设备、通用部件或抽象对象，没有明确实例名或编号，则 entity_name 必须输出空字符串；
- 不要把“定子”“转子”“液压油缸”“吸收器”这类部件名误当作实例名，除非表格明确把它作为具体对象名称；
- 不要为了凑字段强行填写 entity_name。

### 3. field_name
表示当前数据对应的字段名称或属性名称。
要求：
- 普通属性表：提取该行的属性名称，如“驱动方式”“列数”“尺寸”“承受温度”；
- 如果存在层级字段，应合并为更完整的字段语义，例如：
  - “液压油缸 / 数量” -> “液压油缸数量”
  - “炉排片尺寸 / 宽度” -> “炉排片宽度”
- 对条件-结果映射表：
  - 条件维度不要放入 field_name；
  - field_name 只保留结果字段名，例如“定子电流（A）”“定子电流为额定的（%）”。

### 4. field_value
表示当前字段对应的值。
要求：
- 普通属性表：填写该字段对应的具体参数值、规格值、数量值、备注值等；
- 对分阶段参数、复合参数，要把完整关系表达清楚；
- 对条件-结果映射表，要写成“条件值→结果值”的结构化映射字符串，例如：
  - “入口风温40℃→100%；41℃→98.5%；42℃→97%”
- 若同一字段被拆成多段但语义连续，应自动合并后输出；
- 不得编造原文中不存在的数据。

### 5. field_category
表示当前字段所属的业务类别。
请从以下类别中选择最合适的一项：

- 标识信息
- 规格参数
- 结构尺寸
- 材料信息
- 数量信息
- 电气参数
- 热工参数
- 流体参数
- 运行参数
- 性能参数
- 控制保护
- 安装接口
- 运行限制
- 检修维护
- 备注说明
- 其他

分类参考：
- 名称、型号、编号、代号、型式、设备编码 -> 标识信息
- 额定容量、额定功率、绝缘等级、冷却方式、励磁方式、驱动方式、接法、标准规格 -> 规格参数
- 长度、宽度、高度、直径、厚度、行程、角度、间隙、外形尺寸 -> 结构尺寸
- 材质、材料牌号、绝缘材料、叶片材料、管材、涂层 -> 材料信息
- 数量、台数、列数、行数、块数、极数、相数、支路数 -> 数量信息
- 电压、电流、频率、功率、功率因数、励磁电压、励磁电流、阻抗、绝缘电阻 -> 电气参数
- 温度、蒸汽温度、排烟温度、金属温度、热耗、焓值 -> 热工参数
- 压力、流量、真空、背压、压差、水位、风量、风压、蒸发量、流速、介质参数 -> 流体参数
- 转速、负荷、运行方式、运行工况、启停条件、投运条件、工况切换条件 -> 运行参数
- 效率、性能曲线、修正曲线、温度-电流对应关系、压力-流量对应关系、能耗指标 -> 性能参数
- 保护方式、联锁条件、报警值、跳闸值、调节方式、控制逻辑、保护定值 -> 控制保护
- 安装方式、连接方式、接口尺寸、法兰规格、接线方式、进出口方向 -> 安装接口
- 上限值、下限值、允许范围、极限温度、极限压力、禁止条件、安全边界、使用限制 -> 运行限制
- 检修周期、维护要求、润滑要求、更换周期、点检项目 -> 检修维护
- 备注、特殊说明、附加说明、补充条件 -> 备注说明
- 无法明确判断时 -> 其他

### 6. content
表示适合知识库入库和向量检索的一段自然语言内容。
要求：
- 忠实原文，不得编造；
- 语义完整，可脱离原表单独阅读；
- 必须带有表格标题上下文；
- 尽量使用如下句式：
  - “在《{table_title}》中，……”
- 语言专业、客观、清晰、自然；
- 适合后续向量检索与问答召回；
- 若为分阶段参数，应在一段文字中明确表达各阶段对应的数值关系；
- 若为条件-结果映射表，应明确表达“在什么条件下，对应什么结果”。

## 处理规则
1. 先整体理解表格标题与表格主题，再逐行解析。
2. 识别每一行中的：
   - 主体对象
   - 属性名称
   - 子属性或阶段信息
   - 参数值
   - 单位
   - 备注说明
3. 对同一行中重复出现的相同值进行去重，不要机械重复输出。
4. 对明显由 XML/文本抽取导致的格式噪声进行轻量清洗，例如：
   - 合并被错误分开的单位或数字，如“2250m m”应理解为“2250mm”
   - 忽略无意义重复词，如“长度（mm） 长度（mm）”
   - 去除转义噪声，如“\\u3000”
5. 输出内容必须忠实于原始数据：
   - 不得编造不存在的数据；
   - 不得补充表格中未出现的解释性知识；
   - 无法确认的内容宁可保留原意，也不要主观推断。
6. 每一条有效数据输出为一个独立对象。
7. 若某一行本身信息不足以单独成立，但能结合字段层级明确其含义，则允许补足最小必要上下文。
8. 若表格是条件-结果映射表、横向矩阵表或分段表：
   - 先识别条件维度；
   - 再识别结果字段；
   - 每个结果字段输出一个独立对象；
   - 同一条件维度被拆成多个连续区间时，应自动合并为一个完整 field_value。
9. 如果某一行只包含层级路径和参数值，请先还原为完整语义，再填写 field_name 和 content。
10. 如果某一行与上一行或上一级字段强相关，允许继承最小必要上下文，但不得编造不存在的信息。
11. 如果表格中存在同一实体下的多个子部件，应优先使用“更完整、可独立理解”的实体语义作为 entity_type。
12. entity_name 只有在表格中存在明确实例名、设备名、编号、唯一标识时才允许填写；否则必须为空字符串。
13. content 必须显式包含表格标题，不得省略来源上下文。

## 输出要求
- 只输出合法 JSON 数组；
- 不要输出 Markdown；
- 不要输出解释；
- 不要输出分析过程；
- 不要输出除 JSON 数组以外的任何内容；
- 输出必须可以被程序直接解析。

## 输入数据
表格标题：
2.1.2设备技术规范

表格数据：
    {table_data}
  """
